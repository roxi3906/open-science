export type MessagePart =
  | { type: 'text'; text: string }
  | { type: 'skill'; id: string; name: string }
  | {
      type: 'artifact'
      id: string
      name: string
      path: string
      source: 'upload' | 'artifact'
      versionId?: string
    }

